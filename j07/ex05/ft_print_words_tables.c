void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_words_tables(char **tab)
{
	while (*tab != NULL)
	{
		ft_putstr(*tab++);
		ft_putchar('\n');
	}
}
