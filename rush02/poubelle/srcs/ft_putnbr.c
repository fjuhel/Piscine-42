#include "ft_rush.h"

void	ft_putnbr(int nb)
{
	int		n;

	n = nb;
	if (nb == -2147483648)
		ft_putstr("-2147483648");
	if (nb > -2147483648 && nb <= 2147483647)
	{
		if (nb < 0)
		{
			ft_putchar('-');
			n *= -1;
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
			ft_putchar(n + '0');
	}
}
