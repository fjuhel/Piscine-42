char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
    char *str = dest;
    while (n > 0 && *src != '\0')
    {
		*str++ = *src++;
		--n;
    }
    while (n > 0)
    {
		*str++ = '\0';
		--n;
    }
    return (dest);
}
