char * ft_strncat(char *dest, char *src, int nb)
{
	char *str;

	str = dest;
	while (*str != '\0')
		str++;
	while (*src != '\0' && nb-- > 0)
		*str++ = *src++;
	*str = '\0';
	return (dest);
}
